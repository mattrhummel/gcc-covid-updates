<h2>What is dual enrollment, exactly?</h2>
<p class="lead">Dual enrollment at Germanna is an opportunity for your <a href="https://germannacc.staging.wpengine.com/dual-enrollment/in-your-high-school/">high school</a> or home school student to earn college credit while still in high school.</p>
<p>Courses are taught either in the high school or at Germanna. The credit your student earns is transferable to most Virginia colleges and universities. Depending on your school district, college-level courses taken through dual enrollment may be free or at a reduced rate.</p>
